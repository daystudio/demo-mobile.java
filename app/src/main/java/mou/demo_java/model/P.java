package mou.demo_java.model;

public final class P {
    private int id;
    private String c;
    private String t;
    private String u;
    private String i;
    private String a;

    public P(int id, String c, String t, String u, String i, String a){
        this.id = id;
        this.c = c;
        this.t = t;
        this.u = u;
        this.i = i;
        this.a = a;
    }

    public int getId() {
        return id;
    }

    public String getC() {
        return c;
    }

    public String getT() {
        return t;
    }

    public String getU() {
        return u;
    }

    public String getI() {
        return i;
    }

    public String getA() {
        return a;
    }
}